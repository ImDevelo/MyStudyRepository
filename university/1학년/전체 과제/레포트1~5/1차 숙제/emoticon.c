#include <stdio.h>

int main() {

	printf("    ＠^-^＠     \n");// 부끄부끄
	printf("  d º▽º♭    \n");// 최고예요!
	printf("  ㄴ⊙0⊙ㄱ     \n");// (놀람)상상도 못한 정체!
	printf("    > u ~☆     \n");// 윙크~☆
	printf("  (*'▽'*)      \n");// 헤헤 
	printf("   (- 3 -)~♪♩ \n");// 휫바람 
	printf("    ㅠ□ㅠ      \n");// 대성통곡
	printf("    ('Δ')      \n");// 문어
	printf("     xρx       \n");// 깨꼬닥
	printf("    Θ¤Θ      \n");// 후욱후욱 

	return 0;
}
